"""
BrewTrade AI - Documents router.
Export docs, statements, commercial invoices, CARICOM, shipping.
"""
import os
from typing import Any, Dict, List, Optional

from fastapi import APIRouter, Depends, HTTPException, Query
from fastapi.responses import FileResponse, Response
from sqlalchemy import func
from sqlalchemy.orm import Session

from database import get_db
from models import Document


router = APIRouter(prefix="/api/documents", tags=["Documents"])


# Friendly labels for document folder display
DOC_TYPE_LABELS = {
    "export": "Export Documents",
    "statement": "Account Statements",
    "commercial_invoice": "Commercial Invoices",
    "caricom": "CARICOM Certificates",
    "shipping": "Shipping Documents",
}


def _label_for(doc_type: str) -> str:
    return DOC_TYPE_LABELS.get(doc_type, doc_type.replace("_", " ").title())


def _placeholder_text(doc: Document) -> str:
    """Generate a deterministic text placeholder for documents lacking a real file."""
    return (
        f"BrewTrade AI - {_label_for(doc.doc_type)}\n"
        f"========================================\n"
        f"Title       : {doc.title}\n"
        f"Filename    : {doc.filename}\n"
        f"Document ID : {doc.id}\n"
        f"Customer ID : {doc.customer_id}\n"
        f"Issued      : {doc.created_at.isoformat() if doc.created_at else ''}\n\n"
        "This is a generated POC placeholder document. In production this would "
        "be the rendered PDF artifact (commercial invoice, CARICOM origin "
        "certificate, monthly statement, etc.) generated by the document "
        "service and stored in object storage.\n"
    )


